cpu_hogs () { ps wwr -o pid,stat=STATE,%cpu,time,command | head; } # {BH}

# running?: check if a process is running
running? () { # {BH}
	local ps_list="`ps -C "$@" | wc -l` - 1"
	local ps_list=`echo $ps_list | bc`
	if [[ $ps_list -gt 0 ]]; then
		echo "$@ is running"
	else
		echo "$@ is not running"
	fi
}
